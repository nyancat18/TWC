    <h1 class="heading-2">About</h1>
    <p class="paragraph">I started The World in Context out of frustration—with how difficult it is to just follow the news and be informed. There are so many poor sources of information, so many biases to consider. It’s almost like everyone has to be their own journalist. And that takes a lot of time—and a lot of effort. &nbsp;
      <br>
      <br> Let me do that for you. I’ll bring you a daily rundown of the biggest news stories, important stuff that’s hidden beneath the headlines, and why it all matters—every Monday through Friday. I'll be as objective as possible, and do my best to add context to every story. I mark a clear divide between the news and my analysis, so you can draw your own conclusions.
      <br>
